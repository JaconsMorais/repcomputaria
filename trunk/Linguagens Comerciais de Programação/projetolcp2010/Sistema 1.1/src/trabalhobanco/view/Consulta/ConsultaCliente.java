//Botao responsavel por pesquisar o cpr inserido no campo2 e e encaminhalo para
    //o painel de confirmacao de exclusao
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       if(new String(jTextField2.getText()).equals("")==false)if (consultaClienteLogica.consultaClienteCPFs(jTextField2.getText())) {
            try {
                ConsultaClienteLogica.resultSet.next();
                jPanel1.setVisible(true);
                jLabel24.setText(ConsultaClienteLogica.resultSet.getString("CPF"));
                jLabel25.setText(ConsultaClienteLogica.resultSet.getString("Nome"));
                jLabel32.setText(ConsultaClienteLogica.resultSet.getString("Telefone"));
                jLabel26.setText(ConsultaClienteLogica.resultSet.getString("Rua"));
                jLabel1.setText(ConsultaClienteLogica.resultSet.getString("Bairro"));
                jLabel34.setText(ConsultaClienteLogica.resultSet.getString("Cidade"));
                jLabel27.setText(ConsultaClienteLogica.resultSet.getString("Estado"));
                jLabel33.setText(ConsultaClienteLogica.resultSet.getString("Cep"));
            } catch (SQLException ex) {
                Logger.getLogger(ConsultaCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton3ActionPerformed
    }
    //Botao responsavel por fazer a pesquisa de acorndo com o item seleciondado na
    //combobox e mostrar os dados encontrados na tabela
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (jComboBox1.getSelectedItem().toString().equals("CPF")) {
            if (consultaClienteLogica.consultaClienteCPFs(jTextField1.getText())) {
                
                DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
                modelo.setNumRows(0);

                try {

                    while (ConsultaClienteLogica.resultSet.next()) {
                        modelo.addRow(new Object[]{ConsultaClienteLogica.resultSet.getString("CPF"),
                        ConsultaClienteLogica.resultSet.getString("Nome"), ConsultaClienteLogica.resultSet.getString("telefone")});
                    }
                    ConsultaClienteLogica.resultSet.first();
                } catch (SQLException erro) {
                    JOptionPane.showMessageDialog(null, "Erro ao listar no JTable " + erro);
                }
            }
        }
        if (jComboBox1.getSelectedItem().toString().equals("NOME")) {
            if (consultaClienteLogica.consultaClienteNomes(jTextField1.getText())) {

                DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
                modelo.setNumRows(0);

                try {
                    while (ConsultaClienteLogica.resultSet.next()) {
                        modelo.addRow(new Object[]{ConsultaClienteLogica.resultSet.getString("CPF"), ConsultaClienteLogica.resultSet.getString("Nome"), ConsultaClienteLogica.resultSet.getString("telefone")});
                    }
                    ConsultaClienteLogica.resultSet.first();
                } catch (SQLException erro) {
                    JOptionPane.showMessageDialog(null, "Erro ao listar no JTable " + erro);
                }
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed
